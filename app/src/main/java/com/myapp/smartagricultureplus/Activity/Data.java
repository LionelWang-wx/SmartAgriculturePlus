package com.myapp.smartagricultureplus.Activity;

public class Data {
    int Mon;
    int Tue;
    int Wed;
    int Thu;
    int Fri;
    int Sat;
    int Sun;


    public Data(int mon, int tue, int wed, int thu, int fri, int sat, int sun) {
        Mon = mon;
        Tue = tue;
        Wed = wed;
        Thu = thu;
        Fri = fri;
        Sat = sat;
        Sun = sun;
    }

    public int getMon() {
        return Mon;
    }

    public void setMon(int mon) {
        Mon = mon;
    }

    public int getTue() {
        return Tue;
    }

    public void setTue(int tue) {
        Tue = tue;
    }

    public int getWed() {
        return Wed;
    }

    public void setWed(int wed) {
        Wed = wed;
    }

    public int getThu() {
        return Thu;
    }

    public void setThu(int thu) {
        Thu = thu;
    }

    public int getFri() {
        return Fri;
    }

    public void setFri(int fri) {
        Fri = fri;
    }

    public int getSat() {
        return Sat;
    }

    public void setSat(int sat) {
        Sat = sat;
    }

    public int getSun() {
        return Sun;
    }

    public void setSun(int sun) {
        Sun = sun;
    }
}
